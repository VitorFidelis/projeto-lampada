var btn_ON = document.getElementById('btn_ON');
var btn_OFF = document.getElementById('btn_OFF');
var lamp = document.getElementById('lamp');

function Ligar(){
    if(btn_ON.Value = 'ON'){
        lamp.src='./imgs/lampada-On.png';
    }
}

function Desligar(){
    if(btn_OFF.Value ='OFF'){
        lamp.src='./imgs/lampada-Off.jpg';
    }
}